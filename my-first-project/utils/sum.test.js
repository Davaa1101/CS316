// АЛХАМ 1: УЛААН - Эхлээд тестийг бичнэ
const sum = require('./sum');

describe('Sum Function Tests', () => {
  // Үндсэн тест
  test('1 + 2 = 3 гарах ёстой', () => {
    expect(sum(1, 2)).toBe(3);
  });

  test('Сөрөг тоо нэмэх', () => {
    expect(sum(-1, -2)).toBe(-3);
  });

  test('Тэг нэмэх', () => {
    expect(sum(0, 5)).toBe(5);
  });

  // АЛХАМ 4: Тоо биш өгөгдөл өгвөл алдаа заана
  test('Тоо биш өгөгдөл өгвөл алдаа заана', () => {
    expect(() => sum('1', 2)).toThrow('Тоо оруулах шаардлагатай!');
    expect(() => sum(1, '2')).toThrow('Тоо оруулах шаардлагатай!');
    expect(() => sum(null, 2)).toThrow('Тоо оруулах шаардлагатай!');
    expect(() => sum(undefined, 2)).toThrow('Тоо оруулах шаардлагатай!');
  });

  test('Хоёр параметр шаардлагатай', () => {
    expect(() => sum(1)).toThrow('Хоёр параметр шаардлагатай!');
    expect(() => sum()).toThrow('Хоёр параметр шаардлагатай!');
  });
});